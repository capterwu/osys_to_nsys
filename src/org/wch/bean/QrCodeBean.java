package org.wch.bean;

public class QrCodeBean {

    private String wechat_qrcode;
    private String wechat_ticket;
    private String ali_qrcode;
    private String device_id;
    private String add_time;

    public String getWechat_qrcode() {
        return wechat_qrcode;
    }

    public void setWechat_qrcode(String wechat_qrcode) {
        this.wechat_qrcode = wechat_qrcode;
    }

    public String getWechat_ticket() {
        return wechat_ticket;
    }

    public void setWechat_ticket(String wechat_ticket) {
        this.wechat_ticket = wechat_ticket;
    }

    public String getAli_qrcode() {
        return ali_qrcode;
    }

    public void setAli_qrcode(String ali_qrcode) {
        this.ali_qrcode = ali_qrcode;
    }

    public String getDevice_id() {
        return device_id;
    }

    public void setDevice_id(String device_id) {
        this.device_id = device_id;
    }

    public String getAdd_time() {
        return add_time;
    }

    public void setAdd_time(String add_time) {
        this.add_time = add_time;
    }
}
